package com.palle;

public class B 
{
	private int i ;
	private String s;
	private String n;
	private String e;
	
	public B(int i, String s, String n, String e) {
		super();
		this.i = i;
		this.s = s;
		this.n = n;
		this.e = e;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}

	public String getE() {
		return e;
	}

	public void setE(String e) {
		this.e = e;
	}
	
	
	

}
